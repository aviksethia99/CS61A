class VendingMachine:
    """A vending machine that vends some product for some price.

    >>> v = VendingMachine('candy', 10)
    >>> v.vend()
    'Machine is out of stock.'
    >>> v.deposit(15)
    'Machine is out of stock. Here is your $15.'
    >>> v.restock(2)
    'Current candy stock: 2'
    >>> v.vend()
    'You must deposit $10 more.'
    >>> v.deposit(7)
    'Current balance: $7'
    >>> v.vend()
    'You must deposit $3 more.'
    >>> v.deposit(5)
    'Current balance: $12'
    >>> v.vend()
    'Here is your candy and $2 change.'
    >>> v.deposit(10)
    'Current balance: $10'
    >>> v.vend()
    'Here is your candy.'
    >>> v.deposit(15)
    'Machine is out of stock. Here is your $15.'

    >>> w = VendingMachine('soda', 2)
    >>> w.restock(3)
    'Current soda stock: 3'
    >>> w.restock(3)
    'Current soda stock: 6'
    >>> w.deposit(2)
    'Current balance: $2'
    >>> w.vend()
    'Here is your soda.'
    """
    def __init__(self, name, price):
        self.name=name
        self.price=price
        self.balance=0
        self.stock=0

    def vend(self):
        if not self.stock:
            print("'Machine is out of stock.'")

        elif self.balance>=self.price:
            change=self.balance-self.price
            self.balance=0
            self.stock-=1

            if change>0:
                print("'Here is your {0} and ${1} change.'".format(self.name, change))
            else:
                print("'Here is your {0}.'".format(self.name))

        else:
            need=self.price-self.balance
            print("'You must deposit ${0} more.'".format(need))

    def deposit(self, amount):
        if not self.stock:
            print("'Machine is out of stock. Here is your ${0}.'".format(amount))
        else:
            self.balance += amount
            print("'Current balance: ${0}'".format(self.balance))

    def restock(self, stock):
        self.stock += stock
        print("'Current {0} stock: {1}'".format(self.name, self.stock))

